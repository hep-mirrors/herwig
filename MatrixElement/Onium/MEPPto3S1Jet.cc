  Energy2 M2=sqr(M);
  Energy4 um(sqr(uHat()-M2)),tm(sqr(tHat()-M2)),sm(sqr(sHat()-M2));
  double output = 160./81.*pow(Constants::pi*standardModel()->alphaS(scale()),3)*M*O1_*
    (sqr(sHat())*sm+sqr(tHat())*tm+sqr(uHat())*um)/(sm*tm*um);
  // test vs NPB 291 731
