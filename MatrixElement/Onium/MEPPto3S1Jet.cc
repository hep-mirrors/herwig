  // double test = 16.*Constants::pi*sqr(sHat())*
  //   5*Constants::pi*pow(standardModel()->alphaS(scale()),3)*R02*M/9./sqr(sHat())*
  //   (sqr(sHat()/(tHat()-M2)/(uHat()-M2))+sqr(tHat()/(sHat()-M2)/(uHat()-M2))+sqr(uHat()/(sHat()-M2)/(tHat()-M2)));
