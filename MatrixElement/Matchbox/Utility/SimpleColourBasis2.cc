size_t SimpleColourBasis2::prepareBasis(const vector<PDT::Colour>& basis) {

  if ( id33bar.empty() )
    makeIds();

  if ( basis == id88 ) {
    return 1;
  }

  if ( basis == id33bar ) {
    return 1;
  }

  if ( basis == id888 ) {
    return 2;
  }

  if ( basis == id33bar8 ) {
    return 1;
  }

  if ( basis == id8888 ) {
    return 9;
  }

  if ( basis == id33bar88 ) {
    return 3;
  }

  if ( basis == id33bar33bar ) {
    return 2;
  }

  if ( basis == id88888 ) {
    return 44;
  }

  if ( basis == id33bar888 ) {
    return 11;
  }

  if ( basis == id33bar33bar8 ) {
    return 4;
  }

  throw Exception() << "Cannot handle colour configuration" << Exception::abortnow;

  return 0;

}

